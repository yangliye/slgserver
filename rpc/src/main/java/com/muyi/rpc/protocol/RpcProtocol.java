package com.muyi.rpc.protocol;

/**
 * RPC协议常量定义
 * 
 * 协议格式（16字节头 + 数据）：
 * +--------+--------+--------+--------+--------+--------+--------+--------+
 * |  魔数  |序列化  | 压缩   | 消息类型 |         消息ID（8字节）           |
 * +--------+--------+--------+--------+--------+--------+--------+--------+
 * |            数据长度（4字节）              |         数据内容...          |
 * +--------+--------+--------+--------+--------+--------+--------+--------+
 *
 * @author muyi
 */
public class RpcProtocol {
    
    /**
     * 魔数 - 用于快速识别是否是有效的RPC协议包
     */
    public static final byte MAGIC = (byte) 0xCA;
    
    /**
     * 消息头长度（魔数1 + 序列化1 + 压缩1 + 消息类型1 + 消息ID8 + 数据长度4 = 16）
     */
    public static final int HEADER_LENGTH = 16;
    
    /**
     * 最大消息长度（10MB）
     */
    public static final int MAX_FRAME_LENGTH = 10 * 1024 * 1024;
}
